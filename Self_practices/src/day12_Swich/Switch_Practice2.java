package day12_Swich;

public class Switch_Practice2 {

    public static void main(String[] args) {

        String productName = "MacBook";

        switch (productName){
            case"galaxy":
                System.out.println("Samsung");
                break;

            case"iphone":
                System.out.println("Apple");
                break;

            case "MacBook":
                System.out.println("Apple");



        }




    }


}
