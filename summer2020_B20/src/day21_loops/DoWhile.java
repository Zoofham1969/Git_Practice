package day21_loops;

public class DoWhile {
    public static void main(String[] args) {

        boolean result = false;
    // While loop
     //================================

        while(result){
            System.out.println("Hello World");
        }

        System.out.println("===============================");


        // DoWhile loop :
     //========================

        do{ // just do it
            System.out.println("Hello World");
        }while(result);






    }




}
